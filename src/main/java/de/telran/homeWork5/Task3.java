package de.telran.homeWork5;

public class Task3 {
    public static void main(String[] args) {
        double x = 5;
        double y = 6;
        summ(x,y);
        minus(x,y);
        multi(x,y);
        devi(x,y);
    }

    public static void summ(double x, double y) {
        System.out.println(x+y);
    }
    public static void minus(double x, double y) {
        System.out.println(x - y);
    }
    public static void multi(double x, double y) {
        System.out.println(x * y);
    }
    public static void devi(double x, double y) {
        System.out.println(x / y);
    }





}
